public class Java_Operators {
    public static void main(String[] args) {
        boolean b = true;
        System.out.println(b);
        System.out.println(!b);

        boolean a = false;
        System.out.println(!!a);

        boolean c = false || true;
        System.out.println( c);


        boolean a1 = false && true;
        System.out.println( a1);

    }
}
